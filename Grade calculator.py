print("Percentage Grade calculator")
print("*************************")
per=float(input("Enter the mark obtained:"))
if per>=91:
    print("Grade is A1")
elif per>=81:
    print("Grade is A2")
elif per>=71:
    print("Grade is B1")
elif per>=61:
    print("Grade is B2")
elif per>=51:
    print("Grade is C1")
elif per>=41:
    print("Grade is C2")
else:
    print("Grade is D")
